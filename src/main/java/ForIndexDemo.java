public class ForIndexDemo {
    public static void main(String[] args) {
        for (int i=10; i<10; i++) {
            System.out.println("i="+i);
        }  //In ra so chan
        for (int i=2; i<=8; i+=2) {
            System.out.println("i="+i);
        }for (int i=10; i>0; i--) {
            System.out.println("i=" + i);
        }
    }
}
